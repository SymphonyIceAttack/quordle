// ========== 工具函数 ==========
// 根据日期生成确定性的伪随机数
function seededRandom(seed: string): number {
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    const char = seed.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  return Math.abs(hash);
}

// 从数组中根据种子选择确定数量的项目
function selectBySeed<T>(array: T[], count: number, seed: string): T[] {
  const random = seededRandom(seed);
  const result = [...array];
  const selected: T[] = [];

  for (let i = 0; i < count; i++) {
    const index = (random + i * 9973) % result.length;
    selected.push(result[index]);
    result.splice(index, 1);
  }

  return selected;
}

/**
 * 生成Quordle每日单词
 * 替代 AI 生成的版本
 */
export async function generateDailyWordsFromList(
  difficulty: "beginner" | "intermediate" | "expert" = "intermediate",
  date?: string,
) {
  const today = date || new Date().toISOString().split("T")[0];

  // 根据难度映射到词库
  let difficultyMap: "easy" | "medium" | "hard";
  switch (difficulty) {
    case "beginner":
      difficultyMap = "easy";
      break;
    case "expert":
      difficultyMap = "hard";
      break;
    default:
      difficultyMap = "medium";
  }

  // 从词库中选择4个单词
  const words = selectBySeed(FIVE_LETTER_WORDS[difficultyMap], 4, today);

  return {
    words: {
      easy:
        difficultyMap === "easy"
          ? words
          : selectBySeed(FIVE_LETTER_WORDS.easy, 4, `${today}-easy`),
      medium:
        difficultyMap === "medium"
          ? words
          : selectBySeed(FIVE_LETTER_WORDS.medium, 4, `${today}-medium`),
      hard:
        difficultyMap === "hard"
          ? words
          : selectBySeed(FIVE_LETTER_WORDS.hard, 4, `${today}-hard`),
    },
    date: today,
    metadata: {
      generatedAt: new Date().toISOString(),
      source: "wordlist",
      difficulty,
    },
  };
}

/**
 * 生成Squares每日谜题
 * 替代 AI 生成的版本
 */
export async function generateDailySquaresFromList(date?: string) {
  const today = date || new Date().toISOString().split("T")[0];

  // 筛选3-5字母单词（优先3-4字母）
  const threeFourLetterWords = ALL_WORDS.filter(
    (w) => w.length >= 3 && w.length <= 4,
  );
  const fiveLetterWords = ALL_WORDS.filter((w) => w.length === 5);

  // 确保至少选择20个单词：40%来自3-4字母，60%来自5字母
  const targetWords = 35;
  const shortWordCount = Math.floor(targetWords * 0.4); // 14个
  const fiveLetterCount = targetWords - shortWordCount; // 21个

  const selectedShortWords = selectBySeed(
    threeFourLetterWords,
    shortWordCount,
    today,
  );
  const selectedFiveWords = selectBySeed(
    fiveLetterWords,
    fiveLetterCount,
    today,
  );
  const selectedWords = [...selectedShortWords, ...selectedFiveWords];

  // 构建5x5网格（25格）
  const grid: string[] = [];
  const allLetters = selectedWords.join("").toUpperCase().split("");

  // 填充25个格子，如果字母不够则填充随机字母
  for (let i = 0; i < 25; i++) {
    if (i < allLetters.length) {
      grid.push(allLetters[i]);
    } else {
      const randomIndex = seededRandom(today + i) % 26;
      grid.push(String.fromCharCode(65 + randomIndex));
    }
  }

  // 从词库中筛选能在网格中组成的单词
  const findableWords = ALL_WORDS.filter((word) => {
    // 简单检查：单词的所有字母是否在网格中存在（至少出现一次）
    const wordUpper = word.toUpperCase();
    return (
      wordUpper.length >= 3 &&
      wordUpper.length <= 6 &&
      wordUpper.split("").every((letter) => grid.includes(letter))
    );
  });

  return {
    grid,
    words: findableWords.slice(0, 30), // 限制最多30个单词
    coreWords: findableWords.slice(0, 20), // 前20个作为核心单词
    bonusWords: findableWords.slice(20, 30), // 剩余的作为奖励单词
    date: today,
    metadata: {
      generatedAt: new Date().toISOString(),
      source: "wordlist",
    },
  };
}
